# ask the user to enter a number and if divided by 2 gives a remainder of zero
# then it is an even number; otherwise and odd number
#
num01 = input('enter a number : ')
mod = int(num01) % 2
if(mod == 0):
    print ('Your number is an even number')
else:
    print ('Your number is an odd number')
            


